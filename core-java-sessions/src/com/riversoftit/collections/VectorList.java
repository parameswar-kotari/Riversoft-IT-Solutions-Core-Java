package com.riversoftit.collections;
import java.util.Vector;

public class VectorList {
	public static void main(String[]args) {
		Vector<String>lists=new Vector<>();
		//Using the add()method
		lists.add("pooja");
		lists.add("syamala");
		//Using index number
		lists.add("sandhya");
		lists.add("lavanya");
		System.out.println("Vector:" +lists);
	}
}


