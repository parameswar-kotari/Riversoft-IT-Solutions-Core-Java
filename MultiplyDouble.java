public class MultiplyDouble {
   public static void main(String[] args) {
       double x = 15.7;
       double y = 2.1;
       int a = 3;
       double z = x*y;
       double b = a*x;
       System.out.println(x + "*" + y + " = " + z);
       System.out.println(a + "*" + x + " = " + b);
   }
}